public class SouthIndianMovieDirector {

    public static void printDirector(SouthIndianMovie movie) {
        if (movie == SouthIndianMovie.BAAHUBALI) {
            System.out.println("Director of Baahubali: S. S. Rajamouli");
        } 
		else if (movie == SouthIndianMovie.KGF) {
            System.out.println("Director of KGF: Prashanth Neel");
        }
		else if (movie == SouthIndianMovie.VIKRAM) {
            System.out.println("Director of Vikram: Lokesh Kanagaraj");
        } 
		else if (movie == SouthIndianMovie.MASTER) {
            System.out.println("Director of Master: Lokesh Kanagaraj");
        } 
		else if (movie == SouthIndianMovie.RRR) {
            System.out.println("Director of RRR: S. S. Rajamouli");
        } 
		
    }
}